
import SwiftUI

extension Color {
    static let systemGreen = Color(NSColor.systemGreen)
    static let systemRed = Color(NSColor.systemRed)
    static let systemYellow = Color(NSColor.systemYellow)
    static let systemGray = Color(NSColor.systemGray)
    
    static var green: Color { return systemGreen }
    static var red: Color { return systemRed }
    static var yellow: Color { return systemYellow }
    static var gray: Color { return systemGray }
}
